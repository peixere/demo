package cn.gotom.vo;

public class MainInfo
{
	private String id;
	private String username;
	private String userFullname;
	private String logoutUrl;
	private String logoId;
	private String topbgId;
	private String title;
	private String fontStyle;
	private String desktopPanel;

	public String getId()
	{
		return id;
	}

	public void setId(String id)
	{
		this.id = id;
	}

	public String getUsername()
	{
		return username;
	}

	public void setUsername(String username)
	{
		this.username = username;
	}

	public String getUserFullname()
	{
		return userFullname;
	}

	public void setUserFullname(String userFullname)
	{
		this.userFullname = userFullname;
	}

	public String getLogoutUrl()
	{
		return logoutUrl;
	}

	public void setLogoutUrl(String logoutUrl)
	{
		this.logoutUrl = logoutUrl;
	}

	public String getLogoId()
	{
		return logoId;
	}

	public void setLogoId(String logoId)
	{
		this.logoId = logoId;
	}

	public String getTopbgId()
	{
		return topbgId;
	}

	public void setTopbgId(String topbgId)
	{
		this.topbgId = topbgId;
	}

	public String getTitle()
	{
		return title;
	}

	public void setTitle(String title)
	{
		this.title = title;
	}

	public String getFontStyle()
	{
		return fontStyle;
	}

	public void setFontStyle(String fontStyle)
	{
		this.fontStyle = fontStyle;
	}

	public String getDesktopPanel()
	{
		return desktopPanel;
	}

	public void setDesktopPanel(String desktopPanel)
	{
		this.desktopPanel = desktopPanel;
	}

}
